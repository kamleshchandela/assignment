var temp = 20;
if(temp < 0){
    console.log("Freezing Cold");
    
}
else if(temp > 0 && temp <= 15){
    console.log("Very Cold");
    
}
else if(temp > 15 && temp <= 25){
    console.log("Cold");
    
}
else if(temp > 25 && temp <= 35){
    console.log("Warm");
    
}
else if(temp > 35){
    console.log("Hot");
    
}